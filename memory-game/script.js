function convertTime(sec) {
  var hours = Math.floor(sec/3600);
  (hours >= 1) ? sec = sec - (hours*3600) : hours = '00';
  var min = Math.floor(sec/60);
  (min >= 1) ? sec = sec - (min*60) : min = '00';
  (sec < 1) ? sec='00' : void 0;
  
  sec = Math.round(sec*100)/100;

  (min.toString().length == 1) ? min = '0'+min : void 0;    
  (sec.toString().length == 1) ? sec = '0'+sec : void 0;    

  return hours+':'+min+':'+sec;
}

function flipCard() {
  numberOfTurns++;
  logMsg("numberOfTurns: " + numberOfTurns);
  if (isFirstCardOpen) {
    startTime = new Date();
    isFirstCardOpen = false;
    updateTime();
  } else {
    updateTime(false);
  }
  if (lockBoard) return;
  if (this === firstCard) return;

  this.classList.add('flip');

  if (!hasFlippedCard) {
    hasFlippedCard = true;
    firstCard = this;

    return;
  }

  secondCard = this;
  checkForMatch();
}

function checkForMatch() {
  let isMatch = firstCard.dataset.framework === secondCard.dataset.framework;
  if (isMatch) {
    numberOfMatchingPairs++;
    if (totalNumberOfPairs == numberOfMatchingPairs) {
      setTimeout(function () {
        winTime = getGameTime();
        //Show dialog with visual effects
        $( "#win_div" ).dialog( "open" );
        $(".ui-dialog-titlebar").hide();
        $( "#win_res" ).text("You win!!! Your time/turns: " + winTime + "/" + numberOfTurns);

        //change BG color to white: win_div ui-dialog-content ui-widget-content
        $('#win_div.ui-dialog-content').css("background-color","white");
        
        let canvasWidth = $('#win_div.ui-dialog-content').width() - 10;
        let canvasHeight = $('#win_div.ui-dialog-content').height() - $('#win_res').height() - 10;
        initConfetti(canvasWidth, canvasHeight);
        renderConfetti();
       
      }, winTimeout);
    }
  }
  isMatch ? disableCards() : unflipCards();
}

